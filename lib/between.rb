# between: Kollar ifall ett värde är mellan två andra värden
#
# num1 - värde nummer 1 som används
# num2 - värde nummer 2 som används
# num3 - värde nummer 3 som används
#
# Examples
#
# between(3,5,4)
#  #==> True
#
# Retunerar ifall det är sant eller falskt
def between(num1, num2, num3)
    if num1 < num2 || num1 > num3
        return false
    else
        return true
    end
end

